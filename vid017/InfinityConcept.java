public class InfinityConcept {
    public static void main(String[] args) {
        // System.out.println(10/0);                             //No way to represent the infinity in integer arithmetic
        System.out.println(10.0/0);                              //But in floating arithmetic there are two constants: POSITIVE_INFINITY and NEGATIVE_INFINITY
        System.out.println(-10.0/0);
    }
}
